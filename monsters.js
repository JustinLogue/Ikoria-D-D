app.monsterCount = () => {
    var count = 0;
    var countWIP = 0;
    var FinCount = 0;
    Object.keys(monsterManual).forEach(function (item){
    
        count = count + 1
        if (monsterManual[item].name !="" && monsterManual[item].name != undefined && (monsterManual[item].behaviour == undefined ||monsterManual[item].behaviour == "")){
            countWIP= countWIP + 1;
            console.log("In progress: " + monsterManual[item].name)

        }
            if(monsterManual[item].behaviour !=undefined && monsterManual[item].behaviour != ""){
            FinCount =FinCount+1;
        }
    })
    
    const html1= `<p>Total count: ${count}</p>`;
    const htmlwip=`<p>In progess count: ${countWIP}</p>`;
    const htmlfin=`<p>Compeleted count: ${FinCount}</p>`;
    $(".counter").append(html1 + htmlwip + htmlfin);
}

